void exam1()
{
	int arr[3] = { 1,2,3 };
	printf("%p\n", arr);  //주소가 100이라 가정 p는 주소를 출력
	printf("%p\n", arr + 1); //주소   104
	printf("%p \n", arr - 1); //주소   96

	printf("%d\n", arr[1]); //0차가 되므로 값이다 값 = 2
	//--------------------------------------------------
	int num;
	//num = arr;  //올바르지 않은 계산 0차 = 1차 ?
	num = *arr + 1; //올바른 연산
	printf("%d\n", num);   //2
}

void exam2()
{
	int arr[2][3]; //2차원 배열 arr[행][열] [열]은 주소값의 이동을 의미

	printf("%d\n", arr);   //2차원 주소  1703440
	printf("%d\n", arr + 1);  //2차원의 이동  1703452
	printf("%d\n", *arr + 1);  //1차원의 주소 1703444

	//열을 5로 바꾸면 1703424 , 1703444 , 1703428

	//arr + 1를 이동한 역참조(*)를 다시 +2만큼 이동한 역참조(*)
	arr[1][2]; //12바이트가 4바이트, 4바이트 이동 ==> 20바이트 이동
}

