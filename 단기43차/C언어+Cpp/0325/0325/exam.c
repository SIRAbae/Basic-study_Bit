//배열 연산 + for(반복문)
//순회 : 배열의 첫 번째 값부터 마지막 값까지 순차적 접근
void exam5()
{
	int arr[10]; //배열선언

	//배열 초기화 (1~10까지)
	for (int i = 0; i < 10; i++)

	{
		arr[i] = i + 1;
	}
	
	//배열 출력
	for (int i = 0; i < 10; i++)
	{
		printf("%d ", arr[i]);
	}
	printf("\n");

	//=====================================
	int arr1[3][10]; //배열선언 2차원을 할때는 반복문 2개가 필요하다

	//배열 초기화

	int number = 1;
	for (int i = 0; i < 3; i++)
	{
		for (int j = 0; j < 10; j++)
		{
			arr1[i][j] = number++;
		}

		//출력
		for (int i = 0; i < 3; i++)
		{
			for (int j = 0; j < 10; j++)
			{
				printf("%d ", arr1[i][j]);
			}
			printf("\n");
		}
	}

}